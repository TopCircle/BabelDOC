def is_cjk_two_char_word(text: str) -> bool:
    """判断是否为常见中文二字词。"""
    return text in _CJK_TWO_CHAR_WORDS


def is_cjk_three_char_word(text: str) -> bool:
    """判断是否为常见中文三字词。"""
    return text in _CJK_THREE_CHAR_WORDS


def is_cjk_word_boundary(text: str, pos: int) -> bool:
    """判断 pos 位置是否为词组边界。

    Args:
        text: CJK 文本
        pos: 字符位置（在 pos 和 pos+1 之间判断）

    Returns:
        True 表示可以在该位置断行
    """
    if pos <= 0 or pos >= len(text):
        return True

    # 检查三字词
    if pos >= 2:
        word3 = text[pos-2:pos+1]
        if is_cjk_three_char_word(word3):
            return False

    # 检查二字词
    if pos >= 1:
        word2 = text[pos-1:pos+1]
        if is_cjk_two_char_word(word2):
            return False

    return True
